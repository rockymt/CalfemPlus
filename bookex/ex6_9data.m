%-----------Element type----------------------------------------
 ElTp = 'plante';

%-------- Extract topology matrix Edof &-----------------------
%-------- element coordinate matrices -------------------------
 [EDof,GDof,Ex,Ey]=ExtractElInfo(El,ElTp,Coord(:,2:end));	   
 
%----- Draw the fe-mesh as a check of the model -----------------
 figure(1)
 hold on;
