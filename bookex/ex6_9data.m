%----- Stiffness matrix K and load vector f ---------------------
 nDof=max(max(GDof));
 K=sparse(nDof,nDof);
 f=zeros(nDof,1);

%----- Element properties  --------------------------------------
 ptype=1; thick=0.5;
 ep=[ptype thick];
 E=30e6; v=0.25;
 D=hooke(ptype,E,v);
 
%--------------------traction force------------------------
 % TBd=[1 2 6 0 -400 0 -400;
      % 1 6 5 0 -400 0 -400];
 TBd=[2 2 6 -400 0 -400 0;
      2 6 5 -400 0 -400 0];
 f=plant2T2f(f,TBd,El,EDof,Ex,Ey,ep);
 
%----- Assemble Ke into K ---------------------------------------

 for i = 1:size(El,1)
   Ke=plante(Ex(i,:),Ey(i,:),ep,D); 
   [K]=assem(EDof(i,:),K,Ke);
 end
 spy(K)
%----- Solve the system of equations ----------------------------

 bc= [GDof(31,1) 0; GDof(31,2) 0;
      GDof(34,1) 0; GDof(34,2) 0;
	  GDof(23,1) 0; GDof(23,2) 0];   
 [a,r]=solveq(K,f,bc)

%----- Element forces -------------------------------------------
